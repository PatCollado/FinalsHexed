public class Coordinates{
  int row;
  int col;
  String direction;
  public Coordinates(int row, int col, String direction){
    row = this.row;
    col = this.col;
    direction = this.direction;
  }

  public int getRow(){
    return row;
  }

  public int getCol(){
    return col;
  }

  public String getDirection(){
    return direction;
  }
}
